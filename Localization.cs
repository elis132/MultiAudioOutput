    // Add fallback for missing languages
    static Localization()
    {
        // Copy English as fallback for languages without full translations
        foreach (var lang in AvailableLanguages.Keys)
        {
            if (!Translations.ContainsKey(lang))
            {
                Translations[lang] = new Dictionary<string, string>(Translations["en"]);
            }
        }
    }

    public static void SetLanguage(string langCode)
    {
        if (AvailableLanguages.ContainsKey(langCode))
            CurrentLanguage = langCode;
    }

    public static string Get(string key)
    {
        if (Translations.TryGetValue(CurrentLanguage, out var langDict))
        {
            if (langDict.TryGetValue(key, out var value))
                return value;
        }

        // Fallback to English
        if (Translations["en"].TryGetValue(key, out var enValue))
            return enValue;

        return key;
    }

    public static string[] GetChannelOptions()
    {
        return new[] { Get("Stereo"), Get("Left"), Get("Right"), Get("Center") };
    }
